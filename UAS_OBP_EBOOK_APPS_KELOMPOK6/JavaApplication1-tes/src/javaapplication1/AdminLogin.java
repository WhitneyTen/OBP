    private void authenticateUser() {
        // Melakukan otentikasi menggunakan nilai username dan password
        if (getUsername().equals("admin") && getPassword().equals("admin")) {
            // Jika username dan password adalah "admin", arahkan ke halaman MenuEbook
            MenuAdmin admin = new MenuAdmin();
            admin.setVisible(true);

            // Tutup halaman saat ini jika diperlukan
            this.dispose();
        } else {
            // Menampilkan pesan kesalahan jika otentikasi gagal
            JOptionPane.showMessageDialog(this, "Salah, Masukan Ulang Password", "Masukan Ulang Password", JOptionPane.ERROR_MESSAGE);
        }
    }
